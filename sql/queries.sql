--○ 대출 조회 뷰 생성
CREATE OR REPLACE VIEW VIEW_LOAN
AS
SELECT L.LOAN_ID,L.USER_ID, U.LOGIN_ID, U.NAME AS USER_NAME
    , L.BOOK_ID, V.TITLE AS BOOK_TITLE,  L.LOAN_DATE, L.DUE_DATE, L.RETURN_DATE, L.EXT_COUNT
FROM LOANS L JOIN  VIEW_BOOK_INFO V
        ON L.BOOK_ID = V.BOOK_ID
    JOIN USERS U
        ON L.USER_ID = U.USER_ID;
        
        
SELECT *
FROM VIEW_LOAN;


